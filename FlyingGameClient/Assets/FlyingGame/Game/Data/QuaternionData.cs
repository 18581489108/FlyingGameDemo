using System.Collections;

namespace Kurisu.Game.Data
{
    public struct QuaternionData
    {
        public float x;

        public float y;

        public float z;

        public float w;

        public QuaternionData(float x, float y, float z, float w)
        {
            this.x = x;
            this.y = y;
            this.z = z;
            this.w = w;
        }
    }
}

